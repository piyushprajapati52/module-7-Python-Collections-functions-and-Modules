# Access values between index 1 and 5 in a tuple



# Creating a sample tuple
my_tuple = (10, 20, 30, 40, 50, 60, 70)

# Accessing elements from index 1 to 5 (index 5 is not included in slicing)
sliced_tuple = my_tuple[1:6]

# Printing the sliced tuple
print("Elements from index 1 to 5:", sliced_tuple)