# Tuple with integers
int_tuple = (1,2,3,4,5,6)
print("Interger Tuple:", int_tuple)


# Tuple with strings
str_tuple = ("apple","banana","cherry")
print("string tuple:", str_tuple)

# Tuple with floats

float_tuple = (1.1,2.2,16.52)
print("float tuple:", float_tuple)


# Tuple with booleans
bool_tuple = (True,False, True)
print("boolean tuple:", bool_tuple)


# Tuple with None values
none_tuple = (None,None)
print("Non tuple:", none_tuple)