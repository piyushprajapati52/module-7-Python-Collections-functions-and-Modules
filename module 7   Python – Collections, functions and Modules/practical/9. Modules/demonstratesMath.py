# Write a Python program to demonstrate the use of functions from
# the math module

import math

print("Square root of 25:", math.sqrt(25))              
print("2 raised to power 3:", math.pow(2,3))
print("The Factorial of 5:", math.factorial(5))          
print("GCD of 12 and 18:", math.gcd(12,18))
print("ceiling of 4.3", math.ceil(4.3))
print("Floor of 4.7:", math.floor(4.7))
print("Value of pi:", math.pi)                           
